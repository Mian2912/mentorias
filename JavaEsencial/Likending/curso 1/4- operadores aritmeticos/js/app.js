"use strick"

// operadores aritmeticos
var datosA = 10
var datosB = 20

// SUMA +
var suma = datosA + datosB
console.log("la suma de " + datosA + " + " + datosB + " es igual a: " + suma)

// RESTA -
var resta = datosA - datosB
console.log("la resta de " + datosA + " - " + datosB + " es igual a: " + resta)

// MULTIPLICACION *
var multiplicacion = datosA * datosB
console.log("la multiplicacion de " + datosA + " * " + datosB + " es igual a: " + multiplicacion)

// DIVISION /
var division = datosA / datosB
console.log("la division de " + datosA + " / " + datosB + " es igual a: " + division)

//MODULO O RESIDUO %
var modulo = datosA % datosB
console.log("el modulo de " + datosA + " % " + datosB + " es igual a: " + modulo)

// INCREMENTO ++
var incremento = datosA
incremento++
console.log("el incremento de " + datosA + " ++  es igual a: " + incremento)

// DECREMENTO --
var decremento = datosA
decremento--
console.log("el decremento de " + datosA + " --  es igual a: " + decremento)

